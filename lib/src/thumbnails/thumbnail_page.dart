import 'dart:async';
import 'dart:typed_data';

import 'package:flutter/material.dart';
import 'package:squadron/squadron.dart';

import '../../pools.dart';

class ThumbnailPage extends StatefulWidget {
  const ThumbnailPage({Key? key, this.tabBar}) : super(key: key);

  final TabBar? tabBar;

  @override
  State<ThumbnailPage> createState() => _ThumbnailPageState();
}

class _ThumbnailPageState extends State<ThumbnailPage> {
  _ThumbnailPageState();

  List<Uint8List>? _images;
  List<Uint8List>? _thumbnails;

  Future _selectImage() async {
    // resetting UI
    if (_images != null) {
      setState(() {
        _images = null;
        _thumbnails = null;
      });
    }

    // have the user pick a set of images or take pictures
    // in this sample, automatically pick up 3 hardcoded images
    final images = [
      Uint8List.fromList(flutterIcon),
      Uint8List.fromList(flutterIcon),
      Uint8List.fromList(flutterIcon),
    ];

    // get the thumbnails from ThumbnailService via Squadron
    final futures = <Future<Uint8List>>[];
    for (var i = 0; i < images.length; i++) {
      futures.add(Future<Uint8List>(() async {
        Squadron.info('Loading image #$i...');
        final sw = Stopwatch()..start();
        final t = await thumbnailWorkerPool.getThumbnail(images[i], 32, 32);
        sw.stop();
        Squadron.info('Image #$i loaded in ${sw.elapsed}');
        return t;
      }));
    }
    final thumbnails = await Future.wait(futures);

    // do something with the thumbnails, eg. display on UI, send to a backend, write to a file or database...
    setState(() {
      _images = images;
      _thumbnails = thumbnails;
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('SQUADRON SAMPLE - THUMBNAILS'),
        bottom: widget.tabBar,
      ),
      body: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            TextButton(
              onPressed: () => _selectImage(),
              child: const Text('Select Image'),
            ),
            Row(
                children: (_images ?? [Uint8List.fromList(pickIcon)])
                    .map((i) => Expanded(child: Image.memory(i)))
                    .toList()),
            if (_thumbnails != null)
              Row(
                  children: _thumbnails!
                      .map((t) => Expanded(child: Image.memory(t)))
                      .toList()),
          ],
        ),
      ),
    );
  }
}

